import java.lang.Math;

public class MathDemo{

  public static void main(String[] args){
  
    double y = -1.5d;
    int z = -25;
    
    System.out.printf("abs value of y %f\n", Math.abs(y));
    System.out.printf("abs value of z %d\n", Math.abs(z));
        
    double k = -1.987654321;
    System.out.println(k); 
    
  }

}

/*in printf %f specifier has precision up to 6 digits after dot*/
